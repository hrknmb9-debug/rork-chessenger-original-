export function getTimeAgo(dateStr: string, lang: Language): string {
  const now = new Date();
  const date = new Date(dateStr);
  const diffMs = now.getTime() - date.getTime();
  const diffMin = Math.floor(diffMs / 60000);
  const diffHour = Math.floor(diffMs / 3600000);
  const diffDay = Math.floor(diffMs / 86400000);

  if (diffMin < 1) return t('just_now', lang);
  if (diffMin < 60) return `${diffMin}${t('ago_min', lang)}`;
  if (diffHour < 24) return `${diffHour}${t('ago_hour', lang)}`;
  return `${diffDay}${t('ago_day', lang)}`;
}
